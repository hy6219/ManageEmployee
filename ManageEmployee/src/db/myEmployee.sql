CREATE TABLE MYEMP(
	USERID    VARCHAR(20) PRIMARY KEY,
	PWD       VARCHAR(20) NOT NULL,
	NAME      VARCHAR(20) NOT NULL,
	LEV       CHAR(1)     DEFAULT 'A',--A:운영자 B:일반회원
	REGDATE   DATE        DEFAULT SYSDATE,
	GENDER    CHAR(1)     DEFAULT '1',--1:남자 2:여자
	PHONE     VARCHAR(30)
);

COMMENT ON COLUMN MYEMP.USERID IS '사용자 아이디';
COMMENT ON COLUMN MYEMP.PWD    IS '사용자 비밀번호';
COMMENT ON COLUMN MYEMP.NAME   IS '사용자 이름';
COMMENT ON COLUMN MYEMP.LEV    IS '사용자 등급 구분';
COMMENT ON COLUMN MYEMP.REGDATE IS '사용자 등록 날짜';
COMMENT ON COLUMN MYEMP.GENDER IS '사용자 성별';
COMMENT ON COLUMN MYEMP.PHONE IS '사용자 연락처';

